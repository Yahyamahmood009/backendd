     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function appoint_store(Request $request)
    {
        $appointment=new Appointment;
        $appointment->Patient_Name=$request->get('p_name');
        $appointment->Patient_id=$request->get('p_id');
        $appointment->DATE=$request->get('app_date');
        $appointment->save();

        return redirect(url('Patient_Deshboard'));
    }

